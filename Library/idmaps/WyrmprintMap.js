function GetWyrmprintInfo(WyrmprintID, Attribute) {
	return WyrmprintInfoMap[String(WyrmprintID)][Attribute];
}

function WyrmprintIDByName(WyrmprintName) {
	let i = 0;
	while (i < Object.keys(WyrmprintInfoMap).length) {
		const WyrmprintID = Object.keys(WyrmprintInfoMap)[i];
		if (WyrmprintInfoMap[WyrmprintID]['name'] == WyrmprintName) {
			return parseInt(Object.keys(WyrmprintInfoMap)[i]);
		}
		else {
			i++;
		}
	}
	return 1;
}

