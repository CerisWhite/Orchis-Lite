function WyrmprintBuild(ID, Buildup, Augments, PreviousWyrmprintData) {
	var Template = {
        "ability_crest_id": ID,
        "buildup_count": PreviousWyrmprintData['buildup_count'],
        "limit_break_count": PreviousWyrmprintData['limit_break_count'],
        "equipable_count": PreviousWyrmprintData['equipable_count'],
        "hp_plus_count": PreviousWyrmprintData['hp_plus_count'],
        "attack_plus_count": PreviousWyrmprintData['attack_plus_count'],
        "is_new": 0,
        "is_favorite": PreviousWyrmprintData['is_favorite'],
        "gettime": PreviousWyrmprintData['gettime']
	}
	let MaterialList = [];
	if (Buildup != undefined) {
	let i = 0;
	while (i < Buildup.length) {
		switch(Buildup[i]['buildup_piece_type']) {
			case 1: // Unbind
				Template['limit_break_count'] = Buildup[i]['step'];
				break;
			case 6: // Copies
				Template['equipable_count'] = Buildup[i]['step'];
				break;
			case 7: // Level Up
				Template['buildup_count'] = Buildup[i]['step'];
				break;
		}
		i++;
	}
	}
	if (Augments != undefined) {
	let y = 0;
	while (y < Augments.length) {
		switch(Augments[y]['plus_count_type']) {
			case 1:
				Template['hp_plus_count'] = Augments[y]['plus_count'];
			case 2:
				Template['attack_plus_count'] = Augments[y]['plus_count'];
		}
		y++;
	}
	}
	
	return [Template, MaterialList];
}
